export function searchSchools(query: string): string[] {
  if (!query.trim()) return SCHOOLS;
  
  const lowerQuery = query.toLowerCase();
  return SCHOOLS.filter(school =>
    school.toLowerCase().includes(lowerQuery)
  );
}
